#Assignment operators
# +=
a = 4
a += 3
print("Result of a += 3:", a)

# -=
b = 6
b -=9
print("Result of b -= 9:", b)

# *=
c = 2
c *=6
print("Result of c *= 6:", c)

# /=
d = 5
d /= 1
print("Result of d /= 1:", d)
 
 # %=
e = 9
e %= 5
print("Result of e %= 5:", e)

# **=
f = 2
f **= 2
print("Result of f ** 2:", f)

# //=
g = 4
g //= 1
print("Result of g //= 1:", g)


